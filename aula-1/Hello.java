/** 
 * Este código imprime uma mensagem simples no console.
 * O que faz: Exibe "Olá mundo!" na tela.
 * Quando usar: Ideal para primeiros testes em Java e para validar se o ambiente está funcionando corretamente.
 * Como usar: Crie uma classe com o método main e utilize System.out.println para imprimir mensagens.
 * Onde usar: Em qualquer ponto do projeto onde seja necessário exibir mensagens simples no console.
 * Projetos aplicáveis: Primeiros programas em Java, testes de ambiente, introdução à linguagem.
 */

public class Hello { // Declaração da classe pública chamada Hello. Toda aplicação Java começa com uma classe.
    public static void main(String[] args){ // Método principal que inicia a execução do programa. Obrigatório em aplicações Java.
        System.out.println("Olá mundo!"); // Imprime a mensagem "Olá mundo!" no console usando o método println da classe System.
    } // Fecha o bloco do método main. Tudo que está dentro do main é o que será executado.
} // Fecha o bloco da classe Hello. Encerra a definição da classe.
