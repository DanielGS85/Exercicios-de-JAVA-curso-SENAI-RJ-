/** 
 * Este código realiza a soma de dois números inteiros e exibe o resultado.
 * O que faz: Soma dois valores inteiros e imprime o resultado no console.
 * Quando usar: Quando for necessário realizar operações matemáticas simples.
 * Como usar: Declarar variáveis, realizar a operação e exibir o resultado com System.out.println.
 * Onde usar: Em cálculos simples, testes de lógica, ou como parte de funções maiores em sistemas financeiros ou educacionais.
 * Projetos aplicáveis: Calculadoras, sistemas de notas, simuladores de operações matemáticas.
 */

public class Soma { // Declaração da classe pública chamada Soma. Define o escopo do programa.
    public static void main(String[] args){ // Método principal que inicia a execução do programa.
        int a = 5; // Declara a variável inteira a e atribui o valor 5.
        int b = 10; // Declara a variável inteira b e atribui o valor 10.
        int soma = a + b; // Declara a variável soma e armazena o resultado da soma de a + b.
        System.out.println("A soma de " + a + " + " + b + " é: " + soma ); // Imprime a frase com os valores e o resultado da soma.
    } // Fecha o bloco do método main. Encerra a execução principal do programa.
} // Fecha o bloco da classe Soma. Finaliza a definição da classe.
